    public String[] setComboBoxHora(){
        String[] test=new String[hora.size()+1];
        this.horaSel = new int[hora.size()];
        this.Combo_Box_Hora.removeAllItems();
        for(int i=0;i<hora.size();i++){
            test[i]= new String(""+(int)this.hora.get(i));
            this.horaSel[i]= (int)this.hora.get(i);
            this.Combo_Box_Hora.addItem(test[i]);
        }
        
        return test;
    }
